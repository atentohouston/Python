# lo importante a destacar aquí son las palabras clave def y return. 
# La palabra clave def es la que usamos para definir funciones en Python.

def f(x): # Esta primera línea de una función se llama firma de la función.
    return 2 * x + 5

print(f(2))

# Power_Of Function
